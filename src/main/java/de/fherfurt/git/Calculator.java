package de.fherfurt.git;

public class Calculator {

    public int plus(int i, int i1) {
        return i + i1;
    }

    public int minus(int a, int b) {
        return a-b;
    }

    public int mal(int c, int d) {
        return c*d;
    }

    public int geteilt(int e, int f) {
        return e/f;
    }
}