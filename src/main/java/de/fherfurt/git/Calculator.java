package de.fherfurt.git;

public class Calculator {

    public int plus(int _Number1, int _Number2)
    {
        int Result = _Number1 + _Number2;
        return Result;
    }

    public int minus(int _Number1, int _Number2)
    {
        int Result = _Number1 - _Number2;
        return Result;
    }

    public int mal(int _Number1, int _Number2)
    {
        int Result = _Number1 * _Number2;
        return Result;
    }

    public int geteilt(int _Number1, int _Number2)
    {
        int Result = _Number1 / _Number2;
        return Result;
    }

}